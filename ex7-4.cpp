#include <stdio.h>
int time_to_minute(int a, int b); // function prototype

int main()
{
    int n1,n2,time;
    printf("\nEnters your hours: ");
    scanf("%d",&n1);
    printf("\nEnters your minutes: ");
    scanf("%d",&n2);
    time = time_to_minute(n1, n2);
    printf("\nYour time(minute format)is = %d\n",time);
    return 0;
}

int time_to_minute(int a, int b)         // function definition   
{
    int result;
    result = a*60+b;
    return result;                     
    //return statement
}

